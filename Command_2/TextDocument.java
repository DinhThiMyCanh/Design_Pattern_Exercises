package Command_2;

//Receiver
public class TextDocument {
	public void actionBold() {
        System.out.println("Text is now Bold.");
    }

    public void actionItalic() {
        System.out.println("Text is now Italic.");
    }

    public void actionPaste() {
        System.out.println("Text has been Pasted.");
    }
    public void actionCopy() {
        System.out.println("Text has been Copied to clipboard.");
    }
}
